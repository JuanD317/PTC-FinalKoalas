<div><p><h3>La certificación es un procedimiento destinado a que un organismo independiente y autorizado, valide o dictamine la calidad del sistema aplicado por una organización, partiendo y verificando si la misma cumple o no lo dispuesto por un determinado referencial o modelo de calidad, reconocido y oficial.</h3></p></div>
<div><p><h3>¿Qué debe llevar una certificación?</h3></p></div>
<ul class="list-unstyled">
  <li><h3><MARK>1º</MARK> Periodo de validez: en el que se muestra hasta cuando está vigente dicha acreditación. Y es que estas deben revisarse y renovarse cada cierto tiempo para asegurar que todo sigue en el buen camino.</h3></li>
  <li><h3><MARK>2º</MARK> Información sobe los productos o servicios: en la acreditación de calidad encontraremos un detalle acerca de los productos o servicios sobre los que se aplica y los que tiene  valor.</h3></li>
  <li><h3><MARK>3º</MARK> Norma de referencia: es decir, en la que se ha basado para revisar la materia correspondiente. Aquí aparecerán también las características de dicho producto, servicio, procedimiento, etc.</h3></li>
  <li><h3><MARK>4º</MARK> Entidad de certificación: debe figurar la entidad o el organismo que ha emitido el certificado de calidad.</h3></li>
  <li><h3><MARK>5º</MARK> Información sobe la organización: fabricante, proveedor, etc. Sobre la que se acredita.</h3></li>
</ul>
